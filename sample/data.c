int get_tc_sample(struct tc_items * sample, int idx){
    const int sample_size = sizeof(thermocouple_data) / sizeof(struct tc_items);
    if (sample == NULL){
        return -ENODEV;
    }
    if (idx > (sample_size - 1)){
        return -E2BIG;
    }
    memcpy(sample, &thermocouple_data[idx], sizeof(struct tc_items));
    return 0;
}
